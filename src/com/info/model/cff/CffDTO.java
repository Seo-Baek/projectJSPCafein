package com.info.model.cff;

public class CffDTO {

	private int cff_no;
	private String cff_file;
	private String cff_title;
	private String cff_cont1;
	private String cff_cont2;
	
	public int getCff_no() {
		return cff_no;
	}
	public void setCff_no(int cff_no) {
		this.cff_no = cff_no;
	}
	public String getCff_file() {
		return cff_file;
	}
	public void setCff_file(String cff_file) {
		this.cff_file = cff_file;
	}
	public String getCff_title() {
		return cff_title;
	}
	public void setCff_title(String cff_title) {
		this.cff_title = cff_title;
	}
	public String getCff_cont1() {
		return cff_cont1;
	}
	public void setCff_cont1(String cff_cont1) {
		this.cff_cont1 = cff_cont1;
	}
	public String getCff_cont2() {
		return cff_cont2;
	}
	public void setCff_cont2(String cff_cont2) {
		this.cff_cont2 = cff_cont2;
	}
	
	
}
